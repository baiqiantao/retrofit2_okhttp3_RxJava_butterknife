package com.example.ljd.retrofit.bean;


public class User {

    private String name;
    private String email;

    public String getName() {
        return name;
    }

    public void setLogin(String login) {
        this.name = login;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
